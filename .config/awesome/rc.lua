-- {{{ Heat monitor
myheatmon = wibox.widget.textbox()
function heat_status ()
    local output = {} -- output buffer
    local fd = io.popen("cat /sys/class/thermal/thermal_zone0/temp", "r") -- cat first thermal zone
    if fd == nil then
        return
    end
    local line = fd:read()
    local heat = string.match(line, "(%d+)")
    if heat then
        heat = tonumber(heat) / 1000
        local r = 80 -- heat < 20 and hex(0) or hex(255 * (heat - 20) / 80)
        local g = 80 --heat < 20 and hex(255) or hex(255 * (100 - heat) / 80)
        table.insert(output, "<span color=\"#"
            .. r
            .. g
            .. "00\">" .. heat .. "&#8451;</span>")
    end
    return table.concat(output, " ")
end
myheatmon:set_markup(heat_status() .. " ")
my_heatmon_timer = timer({timeout = 19})
my_heatmon_timer:connect_signal("timeout", function()
                                               myheatmon:set_markup(heat_status() .. " ")
                                           end)
my_heatmon_timer:start()
